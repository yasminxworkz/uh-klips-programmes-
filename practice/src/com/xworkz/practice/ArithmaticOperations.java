package com.xworkz.practice;

public class ArithmaticOperations {

	public static void main(String[] args) {
		int a=4;
		int b=7;
		int c=(a+b);
		System.out.println("4+7= "+c);
		
		int x=15; 
		int y=18;
		int z=(x-y);
		System.out.println("15-18= "+z);
		
		int d=5;
		int e=5;
		int f=(d*e);
		System.out.println("5*5=" +f);
		
		int m=49;
		int n=7;
		int p=m/n;
		System.out.println("49/7= "+p);
		 
		
	}
	}


